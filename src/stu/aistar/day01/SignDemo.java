package stu.aistar.day01;

public class SignDemo {
    public static void main(String[] args) {
        // \b - 退格
        System.out.println("xxx\byy");//xxyy

        // \t - 制表符，相当于tab键，默认是空4格
        System.out.println("hello\tworld");

        //输出 james:"wjz nb yyds"

        //双引号
        System.out.println("james:\"wjz nb yyds\"");

        // \ 转义含义
        //单个\
        System.out.println("D:\\ChiXingProject\\j03s_stu");

        //输出\\
        System.out.println("\\\\");
    }
}
